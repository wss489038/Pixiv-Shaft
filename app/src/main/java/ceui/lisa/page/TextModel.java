package ceui.lisa.page;

public class TextModel {

    /**
     * 内容
     */
    public String text;

    /**
     * 字体大小(sp)
     */
    public float textSize;

    /**
     * 字体是否加粗
     */
    public boolean fakeBoldText;

    /**
     * 内容长度
     */
    public int textLength;

    /**
     * 内容高度
     */
    public int height;

    /**
     * 是否顶部标题
     */
    public boolean isChapter;

    /**
     * 是否标题
     */
    public boolean isTitle;

    /**
     * 段落首行
     */
    public boolean partFirstLine;
}